    public void GhepBoTuVungCode(String a, String b, String name)
    {
        Excel e = new Excel();
        HashMapHandling m1 = new HashMapHandling();
        HashMapHandling m2 = new HashMapHandling();
        try{
            m1 = e.readMapEn(MakeFilePath(a));
            m2 = e.readMapEn(MakeFilePath(b));
            m1.connect(m2);
            e.writeMap(m1, MakeFilePath(name));
        }
        catch(Exception ex)
        {
        }
        //Ghép xong 2 file a và b rồi ghi ra file name
        //Xóa a và b khỏi List từ rồi thêm name vào
        Main.listChuDe.add(name);
        //Main.listChuDe.remove(a);
        //Main.listChuDe.remove(b);
        try {
            e.writeList(Main.listChuDe, MakeFilePath("list"));
        } catch (Exception ex) {
        }
    }
    public void TaoBoTuVung(String name)
    {
        Main.listChuDe.add(name);
        Excel e = new Excel();
        HashMapHandling m = new HashMapHandling();
        try{
            e.writeMap(m, MakeFilePath(name));
            e.writeList(Main.listChuDe, MakeFilePath("list"));
        }
        catch(Exception ex)
        {
        }
    }
    public void XoaBoTuVung(String name)
    {
        Main.listChuDe.remove(name);
        Excel e = new Excel();
        HashMapHandling m = new HashMapHandling();
        try{
            e.writeMap(m, MakeFilePath(name));
            e.writeList(Main.listChuDe, MakeFilePath("list"));
        }
        catch(Exception ex)
        {
        }
    }

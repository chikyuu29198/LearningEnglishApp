    private void buttonBDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBDActionPerformed
        Main.selection = cbbCD.getSelectedItem().toString();
        CN = cbbDSTV.getSelectedItem().toString();
        listTu.clear();
        Main.mapAV1.clear();
        Main.LoadAV1();
        if(CN.equals("Ghép Bộ Từ Vựng")){
            listTu = Main.listChuDe;
            listTu.remove(Main.selection);
        }
        else{
        listTu = Main.mapAV1.toArrayList();} 
        cbbTu.removeAllItems();
        for(int i = 0 ;i< listTu.size(); i++)
            cbbTu.addItem(listTu.get(i)); 
        
        if(CN.equals("Thêm Từ")){
            ThemTu();
        }
        else if(Main.selection.equals("Tạo bộ từ vựng") == false && CN.equals("Ghép Bộ Từ Vựng")){
            GhepBoTuVungView();
        }
        else if(Main.selection.equals("Tạo bộ từ vựng") == false && CN.equals("Xóa Từ")){                             
            XoaTu();
           }
        else if(Main.selection.equals("Tạo bộ từ vựng") == false && CN.equals("Sửa Từ")){
            SuaTu();
        }
        else if(Main.selection.equals("Tạo bộ từ vựng") == false && CN.equals("Xóa Bộ Từ Vựng")){
            XoaBoTuVung();
        }
        else{
            
            jPanel2.setVisible(false);
            JOptionPane.showMessageDialog(rootPane, "Bộ từ vựng đã chọn không hợp lệ. Mời bạn vui lòng kiểm tra lại!");
        }

    }//GEN-LAST:event_buttonBDActionPerformed

